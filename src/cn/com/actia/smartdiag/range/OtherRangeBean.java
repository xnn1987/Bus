public class OtherRangeBean extends RangeBean {
	private String categoryName;

	@Override
	public boolean equals(Object o) {
		if (!super.equals(o)) {
			return false;
		}
		if (!(o instanceof OtherRangeBean)) {
			return false;
		}
		OtherRangeBean bean = (OtherRangeBean) o;
		if (!categoryName.equals(bean.categoryName)) {
			return false;
		}
		return true;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
}
